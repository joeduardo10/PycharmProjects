import math
#from math import trunc
num = float(input('Digite um numero: '))
print('o valor digitado {} e a sua parte inteira é : {}'.format(num, math.trunc(num)))
print('a parte inteira é: {:.0f}'.format(num))
print('a parte inteira é: {}'.format(math.floor(num)))
print('o valor digitado {} e a sua parte inteira é : {}'.format(num, int(num)))
